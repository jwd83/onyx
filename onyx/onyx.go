func main() {
	os.Exit(run(os.Args[1:], os.Stdout, os.Stderr))
}

func run(args []string, stdout, stderr io.Writer) int {
	if err := runErr(args, stdout, stderr); err != nil {
		fmt.Fprintf(stderr, "onyx: %v\n", err)
		return 1
	}
	return 0
}

func runErr(args []string, stdout, stderr io.Writer) error {
	if len(args) > 1 {
		return errors.New("usage: onyx [site-root-or-child]")
	}

	start := "."
	if len(args) == 1 {
		start = args[0]
	}

	root, configPath, err := findConfig(start)
	if err != nil {
		return err
	}

	cfg, err := readConfig(root, configPath)
	if err != nil {
		return err
	}

	warnings, err := buildSite(cfg)
	if err != nil {
		return err
	}
	for _, warning := range warnings {
		fmt.Fprintf(stderr, "warning: %s\n", warning)
	}
	fmt.Fprintf(stdout, "Onyx built %d pages into %s\n", countPages(cfg.Root), filepath.Join(cfg.Root, "public"))
	return nil
}

func countPages(root string) int {
	count := 0
	_ = filepath.WalkDir(filepath.Join(root, "public"), func(p string, d fs.DirEntry, err error) error {
		if err == nil && !d.IsDir() && d.Name() == "index.html" {
			count++
		}
		return nil
	})
	if _, err := os.Stat(filepath.Join(root, "index.html")); err == nil {
		count++
	}
	return count
}

func findConfig(start string) (string, string, error) {
	abs, err := filepath.Abs(start)
	if err != nil {
		return "", "", err
	}
	info, err := os.Stat(abs)
	if err != nil {
		return "", "", err
	}
	if !info.IsDir() {
		abs = filepath.Dir(abs)
	}

	for {
		ini := filepath.Join(abs, "onyx.ini")
		if _, err := os.Stat(ini); err == nil {
			return abs, ini, nil
		}
		if hasDefaultSource(abs) {
			// No onyx.ini, but a conventional source folder (docs/, wiki/, ...)
			// marks the site root. The returned path is where an optional
			// onyx.ini would live; readConfig tolerates its absence.
			return abs, ini, nil
		}
		parent := filepath.Dir(abs)
		if parent == abs {
			return "", "", errors.New("no onyx.ini or content directory (doc/, docs/, plan/, plans/, wiki/) found in this directory or its parents")
		}
		abs = parent
	}
}

func hasDefaultSource(dir string) bool {
	for _, name := range defaultSources {
		if info, err := os.Stat(filepath.Join(dir, name)); err == nil && info.IsDir() {
			return true
		}
	}
	return false
}

func readConfig(root, configPath string) (Config, error) {
	// onyx.ini is optional: a missing file leaves every setting at its default.
	values := map[string]string{}
	if configPath != "" {
		parsed, err := parseINI(configPath)
		if err != nil && !os.IsNotExist(err) {
			return Config{}, err
		}
		if parsed != nil {
			values = parsed
		}
	}

	sources, err := resolveSources(root, values)
	if err != nil {
		return Config{}, err
	}

	cfg := Config{
		Root:       root,
		ConfigPath: configPath,
		// An empty title is a sentinel: loadVault later falls back to the home
		// page's title and finally to "Onyx".
		SiteTitle:  valueOr(values, "site_title", ""),
		Sources:    sources,
		Multi:      len(sources) > 1,
		Theme:      valueOr(values, "theme", "theme"),
		Search:     boolOr(values, true, "search", "build.search"),
		Graph:      boolOr(values, true, "graph", "build.graph"),
		ShowSource: boolOr(values, true, "show_source", "publish_raw_markdown"),
	}

	if filepath.IsAbs(cfg.Theme) {
		return Config{}, errors.New("theme must be relative to the site root")
	}

	return cfg, nil
}

// resolveSources determines which content folders to build. An explicit
// source = a, b, c in onyx.ini wins and every listed folder must exist;
// otherwise Onyx auto-detects which of the default folders are present and
// includes all of them.
func resolveSources(root string, values map[string]string) ([]string, error) {
	requested := defaultSources
	explicit := false
	if raw, ok := values["source"]; ok && strings.TrimSpace(raw) != "" {
		requested = splitList(raw)
		explicit = true
	}

	var sources []string
	for _, name := range requested {
		if filepath.IsAbs(name) {
			return nil, errors.New("source must be relative to the site root")
		}
		dir := filepath.Join(root, filepath.FromSlash(name))
		info, err := os.Stat(dir)
		if err != nil {
			if explicit {
				return nil, fmt.Errorf("source directory %q: %w", name, err)
			}
			continue // an auto-detected default that simply isn't present
		}
		if !info.IsDir() {
			if explicit {
				return nil, fmt.Errorf("source %q is not a directory", name)
			}
			continue
		}
		sources = append(sources, path.Clean(filepath.ToSlash(name)))
	}

	if len(sources) == 0 {
		if explicit {
			return nil, errors.New("none of the configured source directories exist")
		}
		return nil, fmt.Errorf("no content directory found (looked for %s)", strings.Join(defaultSources, ", "))
	}
	return sources, nil
}

// splitList parses a comma- or whitespace-separated value into a deduplicated,
// order-preserving list.
func splitList(raw string) []string {
	fields := strings.FieldsFunc(raw, func(r rune) bool {
		return r == ',' || unicode.IsSpace(r)
	})
	seen := map[string]bool{}
	out := make([]string, 0, len(fields))
	for _, f := range fields {
		if f == "" || seen[f] {
			continue
		}
		seen[f] = true
		out = append(out, f)
	}
	return out
}

func parseINI(filename string) (map[string]string, error) {
	data, err := os.ReadFile(filename)
	if err != nil {
		return nil, err
	}
	text := strings.ReplaceAll(string(bytes.TrimPrefix(data, []byte("\xef\xbb\xbf"))), "\r\n", "\n")
	values := map[string]string{}
	section := ""

	for lineNo, raw := range strings.Split(text, "\n") {
		line := strings.TrimSpace(raw)
		if line == "" || strings.HasPrefix(line, "#") || strings.HasPrefix(line, ";") {
			continue
		}
		if strings.HasPrefix(line, "[") && strings.HasSuffix(line, "]") {
			section = strings.ToLower(strings.TrimSpace(line[1 : len(line)-1]))
			continue
		}
		key, val, ok := strings.Cut(line, "=")
		if !ok {
			return nil, fmt.Errorf("%s:%d: expected key = value", filename, lineNo+1)
		}
		key = strings.ToLower(strings.TrimSpace(key))
		val = stripQuotes(strings.TrimSpace(stripInlineComment(val)))
		if section != "" {
			values[section+"."+key] = val
		}
		values[key] = val
	}

	return values, nil
}

func stripInlineComment(s string) string {
	inQuote := rune(0)
	for i, r := range s {
		if r == '\'' || r == '"' {
			if inQuote == 0 {
				inQuote = r
			} else if inQuote == r {
				inQuote = 0
			}
			continue
		}
		if inQuote == 0 && (r == '#' || r == ';') {
			if i == 0 || unicode.IsSpace(rune(s[i-1])) {
				return strings.TrimSpace(s[:i])
			}
		}
	}
	return s
}

func stripQuotes(s string) string {
	if len(s) >= 2 {
		if (s[0] == '"' && s[len(s)-1] == '"') || (s[0] == '\'' && s[len(s)-1] == '\'') {
			return s[1 : len(s)-1]
		}
	}
	return s
}

func valueOr(values map[string]string, key, fallback string) string {
	if v, ok := values[key]; ok && strings.TrimSpace(v) != "" {
		return strings.TrimSpace(v)
	}
	return fallback
}

func boolOr(values map[string]string, fallback bool, keys ...string) bool {
	for _, key := range keys {
		if raw, ok := values[key]; ok {
			switch strings.ToLower(strings.TrimSpace(raw)) {
			case "1", "true", "yes", "on":
				return true
			case "0", "false", "no", "off":
				return false
			}
		}
	}
	return fallback
}

func buildSite(cfg Config) ([]string, error) {
	if err := ensureRootIndexWritable(filepath.Join(cfg.Root, "index.html")); err != nil {
		return nil, err
	}
	if err := ensureNoJekyll(cfg.Root); err != nil {
		return nil, err
	}

	vault, warnings, err := loadVault(cfg)
	if err != nil {
		return warnings, err
	}
	renderVault(vault, &warnings)
	computeBacklinks(vault)

	if err := preparePublic(filepath.Join(cfg.Root, "public")); err != nil {
		return warnings, err
	}
	if err := writeAssets(vault); err != nil {
		return warnings, err
	}
	if err := writePages(vault); err != nil {
		return warnings, err
	}
	if cfg.Search {
		if err := writeSearchIndex(vault); err != nil {
			return warnings, err
		}
	}
	if cfg.Graph {
		if err := writeGraph(vault); err != nil {
			return warnings, err
		}
	}

	return warnings, nil
}

func ensureRootIndexWritable(filename string) error {
	data, err := os.ReadFile(filename)
	if err != nil {
		if os.IsNotExist(err) {
			return nil
		}
		return err
	}
	if bytes.Contains(data, []byte(generatedMarker)) ||
		bytes.Contains(data, []byte(html.EscapeString(generatedMarker))) ||
		isBlankFile(data) {
		return nil
	}
	return fmt.Errorf("%s exists and is not marked as Onyx-generated", filename)
}

func ensureNoJekyll(root string) error {
	filename := filepath.Join(root, ".nojekyll")
	if _, err := os.Stat(filename); err == nil {
		return nil
	} else if !os.IsNotExist(err) {
		return err
	}
	return os.WriteFile(filename, []byte("Generated by Onyx so GitHub Pages serves static files without Jekyll.\n"), 0o644)
}

func isBlankFile(data []byte) bool {
	if len(bytes.TrimSpace(data)) == 0 {
		return true
	}
	if bytes.HasPrefix(data, []byte{0xff, 0xfe}) {
		for _, b := range data[2:] {
			if b != 0 && b != '\r' && b != '\n' && b != '\t' && b != ' ' {
				return false
			}
		}
		return true
	}
	return false
}

func preparePublic(publicDir string) error {
	info, err := os.Stat(publicDir)
	if err == nil {
		if !info.IsDir() {
			return fmt.Errorf("%s exists and is not a directory", publicDir)
		}
		if _, err := os.Stat(filepath.Join(publicDir, publicMarker)); err != nil {
			return fmt.Errorf("%s exists but is missing %s; refusing to overwrite", publicDir, publicMarker)
		}
		if err := os.RemoveAll(publicDir); err != nil {
			return err
		}
	} else if !os.IsNotExist(err) {
		return err
	}

	if err := os.MkdirAll(publicDir, 0o755); err != nil {
		return err
	}
	return os.WriteFile(filepath.Join(publicDir, publicMarker), []byte("generated by Onyx\n"), 0o644)
}

func loadVault(cfg Config) (*Vault, []string, error) {
	vault := &Vault{
		Config:       cfg,
		ByPath:       map[string]*Page{},
		ByBase:       map[string][]*Page{},
		AssetsByPath: map[string]string{},
		AssetsByBase: map[string][]string{},
	}
	var warnings []string

	for _, src := range cfg.Sources {
		sourceDir := filepath.Join(cfg.Root, filepath.FromSlash(src))
		// With a single source, paths are relative to that folder (it builds
		// as-is). With several, they are relative to the site root, so each
		// note carries its source folder as a section prefix.
		relBase := sourceDir
		if cfg.Multi {
			relBase = cfg.Root
		}

		err := filepath.WalkDir(sourceDir, func(abs string, d fs.DirEntry, err error) error {
			if err != nil {
				return err
			}
			if abs == sourceDir {
				return nil
			}
			if shouldSkipName(d.Name()) {
				if d.IsDir() {
					return filepath.SkipDir
				}
				return nil
			}

			relOS, err := filepath.Rel(relBase, abs)
			if err != nil {
				return err
			}
			rel := filepath.ToSlash(relOS)
			if hasSkippedComponent(rel) {
				if d.IsDir() {
					return filepath.SkipDir
				}
				return nil
			}
			if d.IsDir() {
				return nil
			}

			if strings.EqualFold(filepath.Ext(d.Name()), ".md") {
				page, excluded, err := readPage(abs, rel)
				if err != nil {
					return err
				}
				if excluded {
					warnings = append(warnings, rel+": excluded by frontmatter")
					return nil
				}
				vault.Notes = append(vault.Notes, page)
				return nil
			}

			vault.addAsset(rel)
			return nil
		})
		if err != nil {
			return nil, warnings, err
		}
	}

	sort.Slice(vault.Notes, func(i, j int) bool {
		return strings.ToLower(vault.Notes[i].SourceRel) < strings.ToLower(vault.Notes[j].SourceRel)
	})

	for _, note := range vault.Notes {
		note.PageRel = strings.TrimSuffix(note.SourceRel, path.Ext(note.SourceRel))
		vault.ByPath[strings.ToLower(note.PageRel)] = note
		base := strings.ToLower(path.Base(note.PageRel))
		vault.ByBase[base] = append(vault.ByBase[base], note)
	}

	for _, list := range vault.ByBase {
		sort.Slice(list, func(i, j int) bool {
			return strings.ToLower(list[i].SourceRel) < strings.ToLower(list[j].SourceRel)
		})
	}

	vault.Home = chooseHome(vault)
	// Resolve the site title before any generated home is built (generatedHome
	// reads it): an explicit onyx.ini title wins, else the home page's title,
	// else "Onyx".
	if vault.Config.SiteTitle == "" {
		if vault.Home != nil {
			vault.Config.SiteTitle = vault.Home.Title
		} else {
			vault.Config.SiteTitle = "Onyx"
		}
	}
	if vault.Home == nil {
		vault.Home = generatedHome(vault)
		vault.Notes = append([]*Page{vault.Home}, vault.Notes...)
	}
	vault.Home.IsHome = true

	for _, note := range vault.Notes {
		if note.IsHome {
			note.URL = ""
		} else {
			note.URL = "public/" + escapeURLPath(note.PageRel) + "/"
		}
		if note.SourceRel != "" {
			note.SourceURL = escapeURLPath(path.Join(cfg.sourcePrefix(), note.SourceRel))
		}
	}
	if vault.Home.Generated {
		updateGeneratedHome(vault)
	}

	return vault, warnings, nil
}

func shouldSkipName(name string) bool {
	if strings.HasPrefix(name, ".") {
		return true
	}
	switch strings.ToLower(name) {
	case ".obsidian", ".git", ".trash":
		return true
	}
	return false
}

func hasSkippedComponent(rel string) bool {
	for _, part := range strings.Split(rel, "/") {
		if shouldSkipName(part) {
			return true
		}
	}
	return false
}

func readPage(abs, rel string) (*Page, bool, error) {
	data, err := os.ReadFile(abs)
	if err != nil {
		return nil, false, err
	}
	text := normalizeNewlines(stripUTF8BOM(string(data)))
	frontMatter, body := splitFrontMatter(text)

	if strings.EqualFold(frontMatter["publish"], "false") || truthy(frontMatter["draft"]) {
		return nil, true, nil
	}

	title := strings.TrimSpace(frontMatter["title"])
	if title == "" {
		title = firstHeading(body)
	}
	if title == "" {
		title = strings.TrimSuffix(path.Base(rel), path.Ext(rel))
	}

	return &Page{
		SourceRel:   rel,
		SourceAbs:   abs,
		Title:       title,
		Body:        body,
		FrontMatter: frontMatter,
		Outgoing:    map[string]bool{},
	}, false, nil
}

func stripUTF8BOM(s string) string {
	return strings.TrimPrefix(s, "\ufeff")
}

func normalizeNewlines(s string) string {
	s = strings.ReplaceAll(s, "\r\n", "\n")
	return strings.ReplaceAll(s, "\r", "\n")
}

func splitFrontMatter(text string) (map[string]string, string) {
	frontMatter := map[string]string{}
	if !strings.HasPrefix(text, "---\n") {
		return frontMatter, text
	}

	lines := strings.Split(text, "\n")
	for i := 1; i < len(lines); i++ {
		if strings.TrimSpace(lines[i]) == "---" {
			for _, line := range lines[1:i] {
				key, value, ok := strings.Cut(line, ":")
				if !ok {
					continue
				}
				frontMatter[strings.ToLower(strings.TrimSpace(key))] = stripQuotes(strings.TrimSpace(value))
			}
			return frontMatter, strings.Join(lines[i+1:], "\n")
		}
	}
	return map[string]string{}, text
}

func truthy(s string) bool {
	switch strings.ToLower(strings.TrimSpace(s)) {
	case "1", "true", "yes", "on":
		return true
	default:
		return false
	}
}

func firstHeading(body string) string {
	for _, line := range strings.Split(body, "\n") {
		trimmed := strings.TrimSpace(line)
		if level, text, ok := heading(trimmed); ok && level == 1 {
			return stripInlineMarkdown(text)
		}
	}
	return ""
}

func chooseHome(vault *Vault) *Page {
	if page := vault.ByPath["index"]; page != nil {
		return page
	}
	if page := vault.ByPath["home"]; page != nil {
		return page
	}
	return nil
}

func generatedHome(vault *Vault) *Page {
	return &Page{
		Title:     vault.Config.SiteTitle,
		Text:      vault.Config.SiteTitle,
		Excerpt:   "Generated index",
		Outgoing:  map[string]bool{},
		IsHome:    true,
		Generated: true,
	}
}

func updateGeneratedHome(vault *Vault) {
	if vault.Config.Multi {
		updateSectionedHome(vault)
		return
	}

	var b strings.Builder
	b.WriteString("<p>This site was generated from a folder of Markdown notes.</p>\n")
	b.WriteString("<ul>\n")
	for _, note := range vault.Notes {
		if note.Generated {
			continue
		}
		b.WriteString(`<li><a href="`)
		b.WriteString(html.EscapeString(relativeURL(vault.Home, note.URL)))
		b.WriteString(`">`)
		b.WriteString(html.EscapeString(note.Title))
		b.WriteString("</a></li>\n")
	}
	b.WriteString("</ul>\n")
	vault.Home.HTML = template.HTML(b.String())
}

// updateSectionedHome renders the multi-source landing page: one section per
// source folder, each listing its notes. The left-hand nav already nests the
// same folders, so the two stay in step.
func updateSectionedHome(vault *Vault) {
	var b strings.Builder
	b.WriteString("<p>This site collects several sets of notes. Choose a section to start.</p>\n")
	for _, src := range vault.Config.Sources {
		section := strings.ToLower(path.Clean(filepath.ToSlash(src))) + "/"
		var items []*Page
		for _, note := range vault.Notes {
			if note.Generated {
				continue
			}
			if strings.HasPrefix(strings.ToLower(note.PageRel)+"/", section) {
				items = append(items, note)
			}
		}
		if len(items) == 0 {
			continue
		}
		b.WriteString(`<section class="onyx-index-section">` + "\n")
		b.WriteString("<h2>")
		b.WriteString(html.EscapeString(titleCase(path.Base(path.Clean(filepath.ToSlash(src))))))
		b.WriteString("</h2>\n<ul>\n")
		for _, note := range items {
			b.WriteString(`<li><a href="`)
			b.WriteString(html.EscapeString(relativeURL(vault.Home, note.URL)))
			b.WriteString(`">`)
			b.WriteString(html.EscapeString(note.Title))
			b.WriteString("</a></li>\n")
		}
		b.WriteString("</ul>\n</section>\n")
	}
	vault.Home.HTML = template.HTML(b.String())
}

func (v *Vault) addAsset(rel string) {
	key := strings.ToLower(strings.TrimPrefix(path.Clean(rel), "./"))
	v.AssetsByPath[key] = rel
	base := strings.ToLower(path.Base(rel))
	v.AssetsByBase[base] = append(v.AssetsByBase[base], rel)
}

func renderVault(vault *Vault, warnings *[]string) {
	for _, note := range vault.Notes {
		if note.Generated {
			continue
		}
		renderer := &MarkdownRenderer{
			vault:      vault,
			current:    note,
			warnings:   warnings,
			headingIDs: map[string]int{},
			outgoing:   map[string]bool{},
		}
		result := renderer.Render(note.Body)
		note.HTML = template.HTML(result.HTML)
		note.Text = result.Text
		note.Excerpt = excerpt(result.Text, 220)
		note.Headings = result.Headings
		note.Tags = result.Tags
		note.HasMath = result.HasMath
		note.Outgoing = renderer.outgoing
	}
}

func computeBacklinks(vault *Vault) {
	byRel := map[string]*Page{}
	for _, note := range vault.Notes {
		if !note.Generated {
			byRel[note.PageRel] = note
		}
	}
	for _, note := range vault.Notes {
		for target := range note.Outgoing {
			if page := byRel[target]; page != nil && page != note {
				page.Backlinks = append(page.Backlinks, LinkView{
					Title: note.Title,
					URL:   note.URL,
					Path:  note.SourceRel,
				})
			}
		}
	}
	for _, note := range vault.Notes {
		sort.Slice(note.Backlinks, func(i, j int) bool {
			return strings.ToLower(note.Backlinks[i].Title) < strings.ToLower(note.Backlinks[j].Title)
		})
	}
}

func writeAssets(vault *Vault) error {
	publicDir := filepath.Join(vault.Config.Root, "public")
	themeDir := filepath.Join(vault.Config.Root, filepath.FromSlash(vault.Config.Theme))

	if err := writeThemeOrDefault(filepath.Join(themeDir, "style.css"), filepath.Join(publicDir, "onyx.css"), defaultCSS); err != nil {
		return err
	}
	if err := writeThemeOrDefault(filepath.Join(themeDir, "onyx.js"), filepath.Join(publicDir, "onyx.js"), defaultJS); err != nil {
		return err
	}

	staticDir := filepath.Join(themeDir, "static")
	if info, err := os.Stat(staticDir); err == nil && info.IsDir() {
		return copyDir(staticDir, filepath.Join(publicDir, "theme"))
	}
	return nil
}

func writeThemeOrDefault(source, dest, fallback string) error {
	if data, err := os.ReadFile(source); err == nil {
		return os.WriteFile(dest, data, 0o644)
	} else if !os.IsNotExist(err) {
		return err
	}
	return os.WriteFile(dest, []byte(fallback), 0o644)
}

func copyDir(source, dest string) error {
	return filepath.WalkDir(source, func(abs string, d fs.DirEntry, err error) error {
		if err != nil {
			return err
		}
		if abs == source {
			return nil
		}
		if shouldSkipName(d.Name()) {
			if d.IsDir() {
				return filepath.SkipDir
			}
			return nil
		}
		rel, err := filepath.Rel(source, abs)
		if err != nil {
			return err
		}
		out := filepath.Join(dest, rel)
		if d.IsDir() {
			return os.MkdirAll(out, 0o755)
		}
		data, err := os.ReadFile(abs)
		if err != nil {
			return err
		}
		if err := os.MkdirAll(filepath.Dir(out), 0o755); err != nil {
			return err
		}
		return os.WriteFile(out, data, 0o644)
	})
}

func writePages(vault *Vault) error {
	pageTemplate, err := loadTemplateSource(vault, "page.html", defaultPageTemplate)
	if err != nil {
		return err
	}
	homeTemplate, err := loadTemplateSource(vault, "home.html", defaultPageTemplate)
	if err != nil {
		return err
	}

	for _, page := range vault.Notes {
		tpl := pageTemplate
		if page.IsHome {
			tpl = homeTemplate
		}
		if err := writePage(vault, page, tpl); err != nil {
			return err
		}
	}
	return nil
}

type templateSource struct {
	name   string
	source string
}

func loadTemplateSource(vault *Vault, name, fallback string) (templateSource, error) {
	themePath := filepath.Join(vault.Config.Root, filepath.FromSlash(vault.Config.Theme), name)
	source := fallback
	if data, err := os.ReadFile(themePath); err == nil {
		source = string(data)
	} else if !os.IsNotExist(err) {
		return templateSource{}, err
	}
	return templateSource{name: name, source: source}, nil
}

func parseTemplateForPage(vault *Vault, page *Page, source templateSource) (*template.Template, error) {
	funcs := template.FuncMap{
		"asset": func(name string) string {
			return relativeURL(page, "public/"+escapeURLPath(name))
		},
	}
	return template.New(source.name).Funcs(funcs).Parse(source.source)
}

func writePage(vault *Vault, page *Page, source templateSource) error {
	tpl, err := parseTemplateForPage(vault, page, source)
	if err != nil {
		return err
	}
	pageView := *page
	pageView.URL = relativeURL(page, page.URL)
	if page.SourceURL != "" {
		pageView.SourceURL = relativeURL(page, page.SourceURL)
	}
	data := TemplateData{
		Site: SiteView{
			Title: vault.Config.SiteTitle,
		},
		Page:       &pageView,
		Nav:        template.HTML(renderNav(vault, page)),
		Backlinks:  relativeLinkViews(page, page.Backlinks),
		Search:     vault.Config.Search,
		Graph:      vault.Config.Graph,
		ShowSource: vault.Config.ShowSource && page.SourceURL != "",
		Generated:  template.HTML(generatedMarker),
		RootScript: template.JS(strconv.Quote(relativeRoot(page))),
		PageID:     template.JS(strconv.Quote(page.PageRel)),
		HomeURL:    relativeURL(page, ""),
		CSSURL:     relativeURL(page, "public/onyx.css"),
		JSURL:      relativeURL(page, "public/onyx.js"),
	}

	var out bytes.Buffer
	if err := tpl.Execute(&out, data); err != nil {
		return err
	}

	if page.IsHome {
		return os.WriteFile(filepath.Join(vault.Config.Root, "index.html"), out.Bytes(), 0o644)
	}

	dest := filepath.Join(vault.Config.Root, "public", filepath.FromSlash(page.PageRel), "index.html")
	if err := os.MkdirAll(filepath.Dir(dest), 0o755); err != nil {
		return err
	}
	return os.WriteFile(dest, out.Bytes(), 0o644)
}

func relativeLinkViews(from *Page, links []LinkView) []LinkView {
	out := make([]LinkView, 0, len(links))
	for _, link := range links {
		link.URL = relativeURL(from, link.URL)
		out = append(out, link)
	}
	return out
}

